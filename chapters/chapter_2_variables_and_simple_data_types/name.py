name = 'ADA lovelace'
# Strings have a title method, it title cases each word in the string. Meaning it makes the first letter and only the 
# first letter capitalized. 
print(name.title())
# The string methods upper and lower make all the characters of the string uppercase and lowercase respectively.
print(name.upper())
print(name.lower())

# The lower method is particularly useful. You generally wont want to trust the casing inputted by users, so you can make
# all input lowercase, and then adjust to whatever casing is appropriate from there.

